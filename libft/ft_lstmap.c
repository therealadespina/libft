#include "libft.h"

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*tmp;
	t_list	*lilu;

	if (!lst || !f)
		return (NULL);
	if (!(tmp = (t_list*)malloc(sizeof(t_list))))
		return (NULL);
	tmp = f(lst);
	lilu = tmp;
	while (lst->next)
	{
		lst = lst->next;
		if (!(tmp->next = f(lst)))
		{
			free(tmp->next);
			return (NULL);
		}
		tmp = tmp->next;
	}
	return (lilu);
}
