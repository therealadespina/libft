#include "libft.h"

int		ft_atoi(const char *s)
{
	unsigned long int	res;
	int					neg;

	res = 0;
	neg = 1;
	while (*s && (*s == ' ' || *s == '\n' || *s == '\t'
				|| *s == '\v' || *s == '\f' || *s == '\r'))
		s++;
	if (*s == '-')
		neg = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s && *s >= '0' && *s <= '9')
	{
		res = res * 10 + *s - 48;
		if (res >= 9223372036854775807 && neg == 1)
			return (-1);
		else if (res > 9223372036854775807 && neg == -1)
			return (0);
		s++;
	}
	return (res * neg);
}
